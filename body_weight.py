
# BODY wEIGHT CONVERTER
# Gathering users body weight in pounds even with decimal
body_weight = float(input("enter your body weight: "))
# Converting lbs in kgs
Pounds_to_kgs = body_weight * 0.453
# PRINTING THE FOLLOWING MESSAGE WITH THE RESULT OF THE CONVERSION AT 3 DECIMAL POINTS
print(f"your body weight is: {body_weight} lbs and the equivalent in kgs is: {Pounds_to_kgs:.3f} kg. Thank you!")  # The last number is rounded up or down



  